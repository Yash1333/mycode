function checkthenumbers(num){

    if(num>0){
        console.log("no is positive");
    }
    else if(num==0){
        console.log("no is zero");
    }
    else if(num<0){
        console.log("no is negative");
    } else{
        console.log("invalid");
    }

}
checkthenumbers (32);
checkthenumbers (-32);
checkthenumbers (0);





